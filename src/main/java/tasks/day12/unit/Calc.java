package main.java.tasks.day12.unit;

public class Calc {

    public int add (int x, int y) {
        return x + y;
    }
    public int substraction (int x, int y) {
        return x - y;
    }
    public int multiply (int x, int y) {
        return x * y;
    }
    public int divide (int x, int y) {
        return x / y;
    }
}
